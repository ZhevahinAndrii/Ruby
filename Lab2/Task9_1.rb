def calculate_area(vertices)
    area = 0
    n=vertices.length
    (0...n).each do |i|
        x1,y1 = vertices[i]
        x2,y2 = vertices[(i+1)%n]
        area+=(x1+x2)*(y1-y2)
    end
    area = (area.abs)/2.0
    return area
end

